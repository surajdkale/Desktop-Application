    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        String question = Question.getText();
        String opt1 = Opt1.getText();
        String opt2 = Opt2.getText();
        String opt3 = Opt3.getText();
        String opt4 = Opt4.getText();
        String answer = Answer.getText();
        
        try{
            
           Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/swing","root","root");
            PreparedStatement ps = con.prepareStatement("update question set Question=?, Opt1=?, Opt2=?, Opt3=?,Opt4=?, Answer=? where id=?");
            ps.setString(1,question);
            ps.setString(2,opt1);
            ps.setString(3,opt2);
            ps.setString(4,opt3);
            ps.setString(5,opt4);
            ps.setString(6,answer);
            ps.setString(7,id);
            
            ps.executeUpdate();
            
            
            JOptionPane.showMessageDialog(this, "Successfully Updated","Information",JOptionPane.INFORMATION_MESSAGE);
            dispose();
            new UpdateQuestion().setVisible(true);
            
        }
        catch(HeadlessException | SecurityException | SQLException e){
             JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateQuestion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jToggleButton3ActionPerformed

